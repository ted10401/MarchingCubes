    public void SetNodes(bool[] nodes)
    {
        if(nodes == null || nodes.Length != 8)
        {
            return;
        }

        this.nodes = nodes;

        configuration = 0;
        int bit = 1;
        for(int i = 0; i < nodes.Length; i++)
        {
            bit = 1 << i;
            configuration += nodes[i] ? bit : 0;
        }

        GenerateTriangles();
    }

    private void GenerateTriangles()
    {
        m_triangles.Clear();
        validVertices.Clear();
        validTriangles.Clear();

        AddTriagnles(m_marchingTable[configuration]);
        UpdateValidMesh();
    }

    private void AddTriagnles(params int[] indexes)
    {
        for(int i = 0; i < indexes.Length; i++)
        {
            if(indexes[i] == -1)
            {
                break;
            }

            m_triangles.Add(indexes[i]);
        }
    }

    private void UpdateValidMesh()
    {
        validVertices.Clear();
        validTriangles.Clear();

        Dictionary<int, int> trianglePairs = new Dictionary<int, int>();

        int validTriangle = -1;

        for(int i = 0; i < m_triangles.Count; i++)
        {
            if(!trianglePairs.ContainsKey(m_triangles[i]))
            {
                validTriangle++;
                trianglePairs.Add(m_triangles[i], validTriangle);
                validTriangles.Add(validTriangle);

                int edgeA = m_cornerIndexFromEdgeA[m_triangles[i]];
                int edgeB = m_cornerIndexFromEdgeB[m_triangles[i]];

                validVertices.Add(GetInterpolateVerts(edgeA, edgeB));
            }
            else
            {
                validTriangles.Add(trianglePairs[m_triangles[i]]);
            }
        }
    }

    private Vector3 GetInterpolateVerts(int indexA, int indexB)
    {
        float nodeA = nodes[indexA] ? 1 : 0;
        float nodeB = nodes[indexB] ? 1 : 0;

        float t = (lerp - nodeA) / (nodeB - nodeA);
        return nodePositions[indexA] + t * (nodePositions[indexB] - nodePositions[indexA]);
    }
